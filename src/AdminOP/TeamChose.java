    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        teams = new Teams();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        int selectedrow = jTable1.getSelectedRow();
        teams.setTeamID(Integer.parseInt(model.getValueAt(selectedrow, 0).toString()));
        teams.setTeamName(model.getValueAt(selectedrow, 1).toString());
        // JOptionPane.showConfirmDialog(null, "Do you "+teams.getTeamName());
        if (ch == 1) {
            FootballMatchNew.txtHomeTeamId.setText(teams.getTeamID() + "");
            FootballMatchNew.txtHomeTeamName.setText(teams.getTeamName() + "");

        } else if (ch == 2) {
            FootballMatchNew.txtAwayTeamId.setText(teams.getTeamID() + "");
            FootballMatchNew.txtAwayTeamName1.setText(teams.getTeamName() + "");
        }
    }//GEN-LAST:event_jTable1MouseClicked

