    public void tableemp() {
        //new DefaultTableModel(new String[] {"id","name","price","Qty","Total"},0);
        DefaultTableModel model = new DefaultTableModel(new Object[]{"TeamID", "TeamName"}, 0);
        ArrayList<Teams> list = ds.SelectTeams();
        Object rowdata[] = new Object[2];
        //System.out.println("size" + list.size());
        for (int i = 0; i < list.size(); i++) {
            rowdata[0] = list.get(i).getTeamID();
            rowdata[1] = list.get(i).getTeamName();

            model.addRow(rowdata);
        }

        jTable1.setModel(model);
    }

